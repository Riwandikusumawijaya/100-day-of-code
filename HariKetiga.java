
package hari.ketiga;
public class HariKetiga {
    public static void main(String[] args) {
        //manipulasi variable pada pemograman java
        int umur;
        umur = 81;
        System.out.println(umur);
        umur = 96;
        System.out.println(umur);
        umur = 12;
        System.out.println(umur);
        String nama = "riwan";
        System.out.println(nama);
        nama = "burhan";
        System.out.println(nama);
        String nama_depan, nama_belakang, nama_lengkap;
        nama_depan = "riwandi";
        nama_belakang = "kusuma wijaya";
        System.out.println("Nama depan : ");
        System.out.println("Nama Lengkap : ");
        System.out.println(nama_depan);
        System.out.println(nama_belakang);
        nama_lengkap = nama_depan +" "+ nama_belakang;
        System.out.println(nama_lengkap);
    }
    
}
